    private void validateAllField(){
        boolean hasError = false;

    String firstName = firstNameText.getText();
    String lastName = lastNameText.getText();
    int age = (int) ageText.getValue();
    String email = emailText.getText();
    String selectedType = typeSelect.getSelectedItem().toString();
    String message = messageText.getText();
    String gender = "";

    
    if (firstName.isEmpty() || firstName.length() > 26 || !firstName.matches("^[a-zA-Z]+")) {
        JOptionPane.showMessageDialog(null, "Please enter a First Name with: \n1. Length between 1 and 20 characters. \n2. Chararcters from A...Z or a..z.\n3. No special characters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    if (lastName.isEmpty() || lastName.length() > 26 || !lastName.matches("^[a-zA-Z]+")) {
        JOptionPane.showMessageDialog(null, "Please enter a Last Name with: \n1. Length between 1 and 20 characters. \n2. Chararcters from A...Z or a..z.\n3. No special characters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    if (age < 12 || age > 100) {
        JOptionPane.showMessageDialog(null, "Please enter age between 12 and 100 (both inclusive).", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    if (!email.matches("[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
        JOptionPane.showMessageDialog(null, "Please enetr valid email address.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    if (maleSelect.isSelected()) {
        gender = "Male";
    } else if (femaleSelect.isSelected()) {
        gender = "Female";
    } else if (otherSelect.isSelected()) {
        gender = "Other";
    } else {
        JOptionPane.showMessageDialog(null, "Please select a gender!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    if ("Select one...".equals(selectedType)) {
        JOptionPane.showMessageDialog(null, "Please select a patient type!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    //if (date == null) {
      //  JOptionPane.showMessageDialog(this, "Please select a valid date.", "Date Error", JOptionPane.ERROR_MESSAGE);
    //}
    Calendar selectedCalendar = Calendar.getInstance();
    Date selectedDate = dateText.getDate();
    selectedCalendar.setTime(selectedDate);
    Calendar todayDate = Calendar.getInstance();
    int minAge = 12;
    int maxAge = 100;
    Calendar minBirthDate = (Calendar) todayDate.clone();
    minBirthDate.add(Calendar.YEAR, -maxAge);
    Calendar maxBirthDate = (Calendar) todayDate.clone();
    maxBirthDate.add(Calendar.YEAR, -minAge);
    if (selectedCalendar.after(maxBirthDate) || selectedCalendar.before(minBirthDate)) {
        JOptionPane.showMessageDialog(null, "Please enter valid date!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    
    if (message.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter a message!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        hasError = true;
    }

    if (!hasError && i==2) {
       
        newUser.setFirstName(firstName);
        newUser.setLastName(lastName);
        newUser.setAge(age);
        newUser.setEmail(email);
        newUser.setGender(gender);
        newUser.setType(selectedType);
        newUser.setMessage(message);
        newUser.setDate(selectedDate);
        
        detailsPanel newDetailsPanel = new detailsPanel(newUser);
        bottomPanel.add(newDetailsPanel, "details");
        CardLayout layout = (CardLayout) bottomPanel.getLayout();
        layout.show(bottomPanel, "details");
        
        
    }
        
    }
    
    
