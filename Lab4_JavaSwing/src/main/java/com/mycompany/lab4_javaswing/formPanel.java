    private void uploadFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadFileActionPerformed
        // TODO add your handling code here:
        
                JFileChooser fileChooser = new JFileChooser();
                fileChooser.setFileFilter(new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png"));
                int returnValue = fileChooser.showOpenDialog(null);

                if (returnValue == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = fileChooser.getSelectedFile();
                    if (selectedFile.getName().endsWith(".jpg") || selectedFile.getName().endsWith(".jpeg") || selectedFile.getName().endsWith(".png")) {
                        newUser.setImagePath(selectedFile.getAbsolutePath());
                        ImageIcon imageIcon = new ImageIcon(selectedFile.getAbsolutePath());

                        int labelWidth = previewImage.getWidth();
                        int labelHeight = previewImage.getHeight();

                        Image image = imageIcon.getImage();
                        Image scaledImage = image.getScaledInstance(labelWidth, labelHeight, Image.SCALE_SMOOTH);
                        imageIcon = new ImageIcon(scaledImage);
                        //ImageIcon imageIcon = new ImageIcon(selectedFile.getAbsolutePath());
                        previewImage.setIcon(imageIcon);
                        i=2;
                        
                    } else {
                        JOptionPane.showMessageDialog(null, "Please select an image!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                    }
                }

    }//GEN-LAST:event_uploadFileActionPerformed

